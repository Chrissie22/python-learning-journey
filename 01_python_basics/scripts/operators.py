# Let's start with two numbers

num1 = 20
num2 = 5

# -- Arithmetic Operators ---

sum_result = num1 + num2
product_result = num1 * num2
division_result = num1 / num2

print(f"{num1} + {num2} = {sum_result}")
print(f"{num1} * {num2} = {product_result}")
print(f"{num1} / {num2} = {division_result}")

print("___") # This is just a seperator

# --- Comparison Operators ---

# The result of a comparison is always True or False

are_they_equal = (num1 == num2)
is_num1_bigger = (num1 > num2)

print(f"Is {num1} equal to {num2}? {are_they_equal}")
print(f"Is {num1} bigger than {num2}? {is_num1_bigger}")